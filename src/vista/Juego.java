        @Override
    public void actionPerformed(ActionEvent e) {
        JRadioButton boton = (JRadioButton) e.getSource();
        int opcion = 0;
        if(boton == radio1)
        {
            opcion = 0;
        }
        if(boton == radio2)
        {
            opcion = 1;
        }
        if(boton == radio3)
        {
            opcion = 2;
        }
        if(boton == radio4)
        {
            opcion = 3;
        }
        if(opcion == respuesta_correcta)
        {
            System.out.println("Correcto");
        }
        else
        {
            System.out.println("Incorrecto");
        }
        buttonGroup1.clearSelection();
        pintarPregunta();

    }
    
    public String procesarRuta(String ruta){
        return ruta.replace("\\", "/");
    }
    
    public void pintarPregunta(){
        Pregunta temp = ln.banderaAleatoria();
        String ruta;
        String[] respuestas;
        int volver_a_jugar;
        
        if(temp!=null)
        {
            try{
                ruta = procesarRuta(Constantes.RUTA+temp.getRutaImagen());
                lbl_imagen.setIcon(new ImageIcon(getClass().getResource(ruta)));
                pregunta.setText(temp.getPregunta());
                respuesta_correcta = temp.getCorrecta();
                respuestas = temp.getRespuestas();
                radio1.setText(respuestas[0]);
                radio1.setName("0");
                radio2.setText(respuestas[1]);
                radio2.setName("1");
                radio3.setText(respuestas[2]);
                radio3.setName("2");
                radio4.setText(respuestas[3]);
                radio4.setName("3");
            }
            catch(Exception e){
                System.out.println("Error al cargar ruta:"+e);
            }
        }
        else{
            volver_a_jugar = JOptionPane.showConfirmDialog(rootPane, "Â¿Volver a jugar?", "Juego Terminado", JOptionPane.YES_NO_OPTION);
            
            if(volver_a_jugar == JOptionPane.YES_OPTION)
            {
                ln.reiniciarJuego();
                pintarPregunta();
            }
            else{
                System.exit(0);
            }
        }
        
    }
    
